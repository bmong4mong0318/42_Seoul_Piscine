int	ft_is_prime(int nb)
{
	long long	i;
	long long	tmp;

	i = 2;
	tmp = nb;
	if (tmp < 2)
		return (0);
	while (i * i <= tmp)
	{
		if (tmp % i == 0)
			return (0);
		i++;
	}
	return (1);
}
